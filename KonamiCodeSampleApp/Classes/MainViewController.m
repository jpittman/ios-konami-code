#pragma mark -
#pragma mark CheatCode Delegates (for Konami Code)

- (void) didEnterCodeSuccessfully
{
	WebViewController *webViewController = [[WebViewController alloc]
										   initWithNibName:nil bundle:nil];	
	[webViewController setModalTransitionStyle:UIModalTransitionStyleCoverVertical];
	
	// Create the navigation controller and present it modally.
	UINavigationController *navigationController = [[UINavigationController alloc]
													initWithRootViewController:webViewController];
	[self presentModalViewController:navigationController animated:YES];
	
	[navigationController release];
	[webViewController release];
}

